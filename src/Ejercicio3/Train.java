package Ejercicio3;

import java.util.List;
import java.util.ArrayList;



public class Train {
    private String number;
    private String route;
    private List<String> wagonList = new ArrayList<>();

    public Train(String number, String route, List<String> wagonList) {
        this.number = number;
        this.route = route;
        this.wagonList = wagonList;
    }

    public String obtenerNumero() { return this.number; }
    public String obtenerRuta() { return this.route; }
    public String obtenerVagon(String type) {
        for (String wagon : wagonList) {
            if (wagon.obtenerTipo().equals(type)) {
                return wagon;
            }
        }
        return null;
    }
}
