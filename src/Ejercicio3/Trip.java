package Ejercicio3;

public class Trip {
    private String date;
    private String departureTime;
    private String arrivalTime;
    private Train train;


    public String obtenerFecha() { return this.date; }
    public String obtenerHoraSalida() { return this.departureTime; }
    public String obtenerHoraLlegada() { return this.arrivalTime; }
    public Train obtenerTren() { return this.train; }
}
